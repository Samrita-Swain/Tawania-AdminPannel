interface SidebarItemProps {
  href: string;
  icon: React.ReactNode;
  label: string;
  roles?: UserRole[];
}

const SidebarItem = ({ href, icon, label, roles }: SidebarItemProps) => {
  const pathname = usePathname();
  const isActive = pathname === href || pathname.startsWith(`${href}/`);
  const { data: session } = useSession();

  // If roles are specified, check if user has permission
  if (roles && session?.user?.role && !roles.includes(session.user.role as UserRole)) {
    return null;
  }

  return (
    <Link
      href={href}
      className={cn(
        "flex items-center gap-3 rounded-lg px-3 py-2 text-gray-800 transition-all hover:text-white",
        isActive ? "bg-white/10 text-white" : ""
      )}
    >
      <span className="h-5 w-5">{icon}</span>
      <span>{label}</span>
    </Link>
  );
};

